package Pekan3;

public class hewan15 {
    public String hewan;
    public int jumlah;

    public hewan15() {

    }

    public hewan15(String hwn) {
        this.hewan = hwn;
    }

    public hewan15(String hwn, int jml) {
        this.hewan = hwn;
        this.jumlah = jml;
    }

    public String getHewan() {
        return hewan;
    }

    public int getJumlah() {
        return jumlah;
    }
} 
